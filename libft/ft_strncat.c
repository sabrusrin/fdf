#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char		*d1;
	const char	*d2;

	if (n)
	{
		d1 = s1;
		d2 = s2;
		while (*d1)
			d1++;
		while (n-- != 0)
		{
			if ((*d1 = *d2++) == 0)
				break ;
			d1++;
		}
		*d1 = 0;
	}
	return (s1);
}
