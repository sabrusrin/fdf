#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char *pd;

	pd = (unsigned char*)dst;
	while (n--)
		*pd++ = *(unsigned char*)src++;
	return (dst);
}
